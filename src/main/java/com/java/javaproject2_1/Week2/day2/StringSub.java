package com.java.javaproject2_1.Week2.day2;

public class StringSub {
    public static void main(String[] args) {
        String str = "Hello";
        System.out.println(str.substring(0,1)); // H만 출력
        System.out.println(str.substring(0,2)); // He만 출력
        System.out.println(str.substring(1,3)); // el만 출력
    }
}
