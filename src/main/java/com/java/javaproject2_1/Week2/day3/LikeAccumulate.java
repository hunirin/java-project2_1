package com.java.javaproject2_1.Week2.day3;

public class LikeAccumulate {
    public static void main(String[] args) {
        int likeCount = 0;
        likeCount = likeCount +1;
        System.out.println(likeCount); // 좋아요 한 개

        likeCount = likeCount +1;
        System.out.println(likeCount); // 좋아요 두 개

        likeCount = likeCount -1;
        System.out.println(likeCount); // 좋아요 한 개


    }
}
