package com.java.javaproject2_1.Week2.day3;

public class RemainderQuote {
    public static void main(String[] args) {
        int iVal = 1;
        System.out.println(iVal/2);

        // 몫
        int quote = 10 / 3;
        // 나머지
        int remainder = 10 % 3;
        System.out.printf("10 / 3의 몫은 %d이고, 나머지는 %d이다", quote, remainder);
    }
}
