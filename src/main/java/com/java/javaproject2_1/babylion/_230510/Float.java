package com.java.javaproject2_1.babylion._230510;

public class Float {
    public static void main(String[] args) {
//        실수
//        유효자릿수가 7 (유효숫자 갯수가 7, 0을 제외한것)
//        유효자릿수 : 좌측부터 숫자 갯수를 의미

        float f1 = 0.11111111f;
        float f2 = 0.22222222f;
        System.out.println(f1 + f2);

        float f3 = 10.11111111f;
        float f4 = 10.22222222f;
        System.out.println(f3 + f4);
    }
}
