package com.java.javaproject2_1.babylion._230503;

public class ArrayStar {
    public static void main(String[] args) {
         for(int i = 5; i > 0; i--){
             for(int j = 0; j < i; j++){
                 System.out.printf("*");
             }
             System.out.println();
         }
    }
}
