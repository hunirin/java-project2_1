package com.java.javaproject2_1.codeup1051_1100;

import java.util.Scanner;

public class _1079 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while(true) {
            char cha = sc.next().charAt(0);
            if(cha == 'q') {
                System.out.println(cha);
                break;
            } else {
                System.out.println(cha);
            }
        }
    }
}
